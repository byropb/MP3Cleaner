package com.spring.mp3;

public class MP3CleanerOptions {

	private String trackOption = "";
	private String titleOption = "";
	private String artistOption = "";
	private String albumOption = "";
	private String readbyOption = "";
	private String yearOption = "";
	private String lengthOption = "";
	private String sizeOption = "";
	private String modifiedOption = "";

	public String getTrackOption() {
		return trackOption;
	}

	public void setTrackOption(String trackOption) {
		this.trackOption = trackOption;
	}

	public String getTitleOption() {
		return titleOption;
	}

	public void setTitleOption(String titleOption) {
		this.titleOption = titleOption;
	}

	public String getArtistOption() {
		return artistOption;
	}

	public void setArtistOption(String artistOption) {
		this.artistOption = artistOption;
	}

	public String getAlbumOption() {
		return albumOption;
	}

	public void setAlbumOption(String albumOption) {
		this.albumOption = albumOption;
	}

	public String getReadbyOption() {
		return readbyOption;
	}

	public void setReadbyOption(String readbyOption) {
		this.readbyOption = readbyOption;
	}

	public String getYearOption() {
		return yearOption;
	}

	public void setYearOption(String yearOption) {
		this.yearOption = yearOption;
	}

	public String getLengthOption() {
		return lengthOption;
	}

	public void setLengthOption(String lengthOption) {
		this.lengthOption = lengthOption;
	}

	public String getSizeOption() {
		return sizeOption;
	}

	public void setSizeOption(String sizeOption) {
		this.sizeOption = sizeOption;
	}

	public String getModifiedOption() {
		return modifiedOption;
	}

	public void setModifiedOption(String modifiedOption) {
		this.modifiedOption = modifiedOption;
	}

}
